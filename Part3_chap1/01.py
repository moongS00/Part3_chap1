'''
<다양한 자료구조>
리스트(List)
튜플(Tuple) : 리스트와 거의 같지만, 한번 내용이 정해지면 변경 불가능
딕셔너리(Dic) : 키값과 벨류값
세트(Set) : 중복된 값 허용 안함

'''

List = ['홍길동', '박찬호', '이용규', '박승철', '김지은']
Tuple = ('홍길동', '박찬호', '이용규', '박승철', '김지은')
Dic = {'홍길동':1, '박찬호':2, '이용규':3, '박승철':4, '김지은':5}
Set = {'홍길동', '박찬호', '이용규', '박승철', '김지은'}

print(List)
print(type(List))
print('\n')
print(Tuple)
print(type(Tuple))
print('\n')
print(Dic)
print(type(Dic))
print('\n')
print(Set)
print(type(Set))