# 리스트의 특정 위치에 아이템 추가 : insert()

st = ['김성예', '신경도', '박기준', '최승철', '황동석']
print('st : {}'.format(st))
print('st의 길이 : {}'.format(len(st)))
print('st의 마지막 인덱스 : {}'.format(len(st) - 1))

st.insert(3, '강호동')
print('st : {}'.format(st))
print('st의 길이 : {}'.format(len(st)))
print('st의 마지막 인덱스 : {}'.format(len(st) - 1))

